import java.util.Scanner;

public class IfTest {

	public static void main(String[] args) {
		System.out.println("Enter a number : ");
		Scanner s=new Scanner(System.in);
		int num=s.nextInt();
		
		if(num<0) {
			System.out.println("Number is negative");
		}else {
			System.out.println("Number is positive");
		}

	}

}
